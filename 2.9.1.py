print("This is a program to convert Celsius temps to Fahrenheit ")
def main():
    celsius = float(input("What is the Celsius temprature? "))
    fahrenheit = 9/5 * celsius + 32
    print("The temprature is ", fahrenheit, "degrees Fahrenheit")

main()
